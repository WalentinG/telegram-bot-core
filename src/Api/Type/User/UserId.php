declare(strict_types = 1);

namespace ServiceBus\TelegramBot\Api\Type\User;

/**
 * User identifier.
 */
final class UserId
{
    /**
     * @var string
     */
    private $value;

    public function __construct(string $value)
    {
        $this->value = $value;
    }

    public function toString(): string
    {
        return $this->value;
    }
}
